
public class Info  {
	int letter;
	int number;

	public Info(int letter, int number) {
		this.letter = letter;
		this.number = number;
	}

	public int getLetter() {
		return letter;
	}

	public void setLetter(int letter) {
		this.letter = letter;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
 
	 
	
}
